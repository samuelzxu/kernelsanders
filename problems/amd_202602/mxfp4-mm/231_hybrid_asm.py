# ── Preshuffle kernel (fast for K<=512 and K=7168) ──
_bc = [None, None, None]
def _ps_gemm(A, Bsh, Bssh, m, k, n):
    key = (Bsh.data_ptr(), Bssh.data_ptr())
    if key != _bc[0]:
        _bc[0] = key
        _bc[1] = Bsh.view(torch.uint8).reshape(n // 16, (k // 2) * 16)
        _bc[2] = Bssh.view(torch.uint8)[:n, :].contiguous().reshape(n // 32, k)
    return gemm_a16wfp4_preshuffle(A, _bc[1], _bc[2], prequant=True, dtype=torch.bfloat16)


# ── ASM kernel path for K=1536 M=256 ──
def _asm_gemm(A, B_shuffle, B_scale_sh, m, k, n):
    """Use aiter.gemm_a4w4 with shuffled data (ASM CK kernels)."""
    A_q, A_scale = dynamic_mxfp4_quant(A)
    A_scale_sh = e8m0_shuffle(A_scale)
    out = aiter.gemm_a4w4(
        A_q.view(dtypes.fp4x2),
        B_shuffle,
        A_scale_sh.view(dtypes.fp8_e8m0),
        B_scale_sh,
        dtype=dtypes.bf16,
        bpreshuffle=True,
    )
    return out


@torch.inference_mode()
def custom_kernel(data: input_t) -> output_t:
    A, B, B_q, B_shuffle, B_scale_sh = data
    m, k = A.shape
    n = B.shape[0]

    # For K=1536 M=256, try ASM path (CK kernels are highly optimized)
    # Despite separate quant overhead, the GEMM kernel itself may be faster
    if k == 1536 and m == 256:
        return _asm_gemm(A, B_shuffle, B_scale_sh, m, k, n)

    # Preshuffle for everything else
    return _ps_gemm(A, B_shuffle, B_scale_sh, m, k, n)
